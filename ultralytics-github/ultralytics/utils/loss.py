def mask_to_bbox(mask):
    # Find the indices where mask is 1
    #pdb.set_trace()
    coords = torch.nonzero(mask, as_tuple=False)
    
    if coords.size(0) == 0:
        # No positive values found, return an empty bounding box
        return torch.zeros(4, dtype=mask.dtype).to(mask.device), torch.tensor(1, dtype=mask.dtype).to(mask.device)

    # Get the minimum and maximum x and y coordinates
    y_min, x_min = torch.min(coords, dim=0)[0]
    y_max, x_max = torch.max(coords, dim=0)[0]

    # Calculate width and height
    width = x_max - x_min
    height = y_max - y_min
    
    # Calculate area
    area = width * height
    
    # Return the bounding box in (x_min, y_min, x_max, y_max) format
    return torch.stack([x_min, y_min, x_max, y_max]), area


